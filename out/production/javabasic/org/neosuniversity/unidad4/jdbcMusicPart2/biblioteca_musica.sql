-- estructura de base de datos para biblioteca
DROP DATABASE IF EXISTS `biblioteca`;
CREATE DATABASE IF NOT EXISTS `biblioteca`
USE `biblioteca`;

--  estructura para tabla biblioteca.cantante
DROP TABLE IF EXISTS `cantante`;
CREATE TABLE IF NOT EXISTS `cantante` (
  `ID_CANTANTE` int(11) NOT NULL, 
  `CANTANTE` varchar(50) NOT NULL,
  `EDAD` int(11) DEFAULT NULL,
  `DIRECCION` varchar(150) DEFAULT NULL
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8;

ALTER TABLE `cantante` ADD PRIMARY KEY (`ID_CANTANTE`);
ALTER TABLE `cantante`CHANGE COLUMN `ID_CANTANTE` `ID_CANTANTE` INT(11) NOT NULL AUTO_INCREMENT FIRST;

--  estructura para tabla biblioteca.disco
DROP TABLE IF EXISTS `disco`;
CREATE TABLE IF NOT EXISTS `disco` (
  `ID_DISCO` int(11) NOT NULL,
  `ID_CANTANTE` int(11) NOT NULL DEFAULT 1,
  `DISCO` varchar(150) NOT NULL DEFAULT '0',
  `ANNO_LANZAMIENTO` int(11) DEFAULT 0
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8;

ALTER TABLE `disco` ADD PRIMARY KEY (`ID_DISCO`);
ALTER TABLE `disco` ADD CONSTRAINT `FK_disco_cantante` FOREIGN KEY (`ID_CANTANTE`) REFERENCES `cantante` (`ID_CANTANTE`);
ALTER TABLE `disco`CHANGE COLUMN `ID_DISCO` `ID_DISCO` INT(11) NOT NULL AUTO_INCREMENT FIRST;

--  estructura para tabla biblioteca.cancion
DROP TABLE IF EXISTS `cancion`;
CREATE TABLE IF NOT EXISTS `cancion` (
  `ID_CANCION` int(11) NOT NULL,
  `ID_DISCO` int(11) NOT NULL DEFAULT 1,
  `CANCION` varchar(50) NOT NULL DEFAULT '0',
  `DURACION` float DEFAULT 0
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8;

ALTER TABLE `cancion` ADD PRIMARY KEY (`ID_CANCION`);
ALTER TABLE `cancion` ADD  CONSTRAINT `FK_cancion_disco` FOREIGN KEY (`ID_DISCO`) REFERENCES `disco` (`ID_DISCO`);
ALTER TABLE `cancion`CHANGE COLUMN `ID_CANCION` `ID_CANCION` INT(11) NOT NULL AUTO_INCREMENT FIRST;

